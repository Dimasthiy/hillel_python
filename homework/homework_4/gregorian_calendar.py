age = int(input("Введіть рік: "))

if 1800 < age < 1_000_000:
    if age % 4 == 0:
        print(age, "Високосний рік")
    elif age % 100 != 0:
        print(age, "Рік не є високосним")
    elif age % 400 == 0:
        print(age, "Високосний рік")
    else:
        print(age, "Рік не є високосним роком")
else:
    print("Рік не відповідає умовам діапазон 1800 до 1_000_000")